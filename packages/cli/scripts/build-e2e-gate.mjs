// Build a TEST-ONLY copy of the compiled CLI at packages/cli/dist-e2e/ whose
// human-presence gate is deterministically controllable, so the black-box e2e
// suite can exercise the admit / pull / secure-mode flows that otherwise require
// a live Touch ID dialog no CI or agent can satisfy.
//
// SECURITY: this artifact must NEVER ship. It lives in dist-e2e/, which is:
//   - outside packages/cli/package.json "files" (["dist"]) → never in the tarball,
//   - listed in .gitignore,
//   - asserted absent from `npm pack` by e2e/packaging-guard.e2e.test.ts.
// Production dist/ is untouched and keeps the real macOS LocalAuthentication gate.
//
// The only change vs. the shipped binary is dist-e2e/localauth.js: presenceAuthorize
// becomes `process.env.LOCKIT_E2E_GATE === "allow"`. That env var has NO meaning in
// the shipped package (the shipped code never reads it), so it is not a backdoor —
// it only does anything against this throwaway build.
import { cpSync, rmSync, existsSync, copyFileSync, writeFileSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const CLI = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const DIST = join(CLI, "dist");
const DIST_E2E = join(CLI, "dist-e2e");

if (!existsSync(join(DIST, "index.js"))) {
  console.error("build-e2e-gate: dist/ not built yet — run `pnpm -r build` first.");
  process.exit(1);
}

rmSync(DIST_E2E, { recursive: true, force: true });
cpSync(DIST, DIST_E2E, { recursive: true });

// Preserve the real compiled gate module, then overlay presenceAuthorize.
copyFileSync(join(DIST_E2E, "localauth.js"), join(DIST_E2E, "localauth.real.js"));

const overlay = `// E2E OVERLAY — generated by scripts/build-e2e-gate.mjs. NEVER SHIPPED.
// Re-exports the real gate factory; replaces only presenceAuthorize with a
// deterministic, env-controlled stand-in so e2e tests can simulate a present or
// absent human without a Touch ID dialog. Shipped dist/ never contains this file.
export { makeAuthorize } from "./localauth.real.js";
export const presenceAuthorize = async () => process.env.LOCKIT_E2E_GATE === "allow";
`;
writeFileSync(join(DIST_E2E, "localauth.js"), overlay, "utf8");

console.log("build-e2e-gate: wrote", DIST_E2E, "(gate honors LOCKIT_E2E_GATE=allow|deny)");
